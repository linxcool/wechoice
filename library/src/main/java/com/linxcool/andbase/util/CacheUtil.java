    /**
     * 保存 byte数据 到 缓存中 无期限
     *
     * @param key   保存的key
     * @param value 保存的数据
     */
    public void put(String key, byte[] value) {
        File file = manager.newFile(key);
        FileOutputStream out = null;
        try {
            out = new FileOutputStream(file);
            out.write(value);
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (out != null) {
                try {
                    out.flush();
                    out.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            manager.put(file);
        }
    }

    /**
     * 保存 byte数据 到 缓存中 有期限
     *
     * @param key      保存的key
     * @param value    保存的数据
     * @param saveTime 保存的时间，单位：秒
     */
    public void put(String key, byte[] value, int saveTime) {
        put(key, DataUtil.newByteArrayWithDateInfo(saveTime, value));
    }

    /**
     * 获取 byte 数据 拥有过期判断能力 过期返回null并删除旧缓存
     *
     * @param key
     * @return byte 数据
     */
    public byte[] getAsBinary(String key) {
        RandomAccessFile rafile = null;
        boolean removeFile = false;
        try {
            File file = manager.get(key);
            if (!file.exists())
                return null;
            rafile = new RandomAccessFile(file, "r");
            byte[] byteArray = new byte[(int) rafile.length()];
            rafile.read(byteArray);
            if (!DataUtil.isDue(byteArray)) {
                return DataUtil.clearDateInfo(byteArray);
            } else {
                removeFile = true;
                return null;
            }
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        } finally {
            if (rafile != null) {
                try {
                    rafile.close();
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
            if (removeFile)
                remove(key);
        }
    }

